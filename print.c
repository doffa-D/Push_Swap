#include "push_swap.h"

void print(t_data *data)
{
	int i = data->argc - 1;
	ft_printf("-----------------------------------------\n");
	while (i >= 0)
	{
		ft_printf("|\t%d [%d] \t\t|\t%d [%d]\t|\n", data->stack_a[i],i, data->stack_b[i],i);
		i--;
	}
	ft_printf("|\n--------|  A  |--------------|  B  |-----\n");

}